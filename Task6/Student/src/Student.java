public class Student {
    private String name;
    private int age;

    public Student() {
        name = "Thuy";
        age = 19;
    }
    public Student(String Name, int Age) {
        name = Name;
        age = Age;
    }
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }

}
